
public class Job {
	private String jobName;
	private int income;
	
	public Job (String jobName, int income) {
		this.jobName = jobName;
		this.income = income;
		
	}
	public String getJobName() {
		return this.jobName;
	}
	
	public int getIncome() {
		return this.income;
	}
	
	
	
	
}
